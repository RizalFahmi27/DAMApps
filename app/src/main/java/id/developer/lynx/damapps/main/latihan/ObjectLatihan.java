public class ObjectLatihan {

    private String text, gambar;

    public ObjectLatihan(String text, String gambar){
        this.text = text;
        this.gambar = gambar;
    }

    public String getGambar() {
        return gambar;
    }

    public String getText() {
        return text;
    }
}
